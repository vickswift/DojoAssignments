x = (1..7)

# .include?(value) => true or false
puts "It does include 5!" if x.include?(5)

# .last => returns the last object in range
puts "#{x.last} is the last object in the range"

# .max => returns the maximum value in range
puts "#{x.max} is the maximum number in the range"

# .min => returns the minimum value in range
puts "#{x.min} is the minimum number in the range"
